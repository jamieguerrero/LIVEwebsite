		<p>Who is a Delegate? The students of today who are becoming the business leaders of tomorrow, the ones who will be solving the world’s problems, the ones who will innovate and transform how the world thinks, the ones who will use tools such as social media to create change, are you up for the challenge?</p>

		<p>There is a saying “The A student will end up working for the C student”. How could this possibly be true? The A students clearly demonstrates the knowledge and intelligence needed to comprehend the course material where as the C student is below average. Taking a step back and analyzing the situation we find that there are some skills that cannot be obtained through formal education; skills such as teamwork, communication, leadership and drive.</p>

		<p>In today’s world, to be successful in anything you do, you must be different. You must offer something that others don’t have, and stick out from the crowd. Take the opportunity that LIVE Competition presents to you and be one step ahead of the game.</p>

		<div id="delegatesbenefits" class="page-header smoothScroll"><h1>Benefits</h1></div>
		<p>Located at the heart of Canada’s financial district is University of Toronto Scarborough’s premier undergraduate business competition. LIVE brings the brightest from coast to coast to compete in a competition fabricated by our very own executive team. Experience a competition like no other, one that will engage and revitalize your passion for achievement. Build new skills that will carry on through the rest of your career and differentiate yourself from the rest.</p>
		<p>Indulge in the numerous opportunities to network with the next generation of business leaders and corporate representatives at Corporate Connections Evening. Take the first step in the door by building relationships with recruiters from: Scotiabank, KPMG, CMA Ontario, CGA, and many more! Be inspired by keynote speakers who share insight on their story to the top.</p>
		<p>It’s time to get your feet wet, experience the most unique competition you will ever attend.</p>

