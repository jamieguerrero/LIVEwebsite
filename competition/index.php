		<div id="overview" class="page-header smoothScroll"><h1>Overview</h1></div>
		<p class="lead">LIVE is a two-day national business competition hosted by the University of Toronto Scarborough. </p>
		<p>Held in downtown Toronto, LIVE brings together the brightest and most ambitious undergraduate business students from across Canada. 
			A significant component of the competition is an intense competition spanning all major areas of business, 
			which challenges delegates to apply their knowledge in a real world simulated business environment. 
			 LIVE provides as a platform for delegates to explore their potential as future business leaders.</p>

		<!-- ================================================ Competition ================================================ -->

		<div id="competition" class="page-header smoothScroll"><h1>Competition</h1></div>
		<p class="lead">LIVE puts you to the test in a practical learning environment like no other</p>

		<p>A fundamental aspect of LIVE is an intense, multidisciplinary business competition that allows delegates the unique opportunity to face tomorrow’s executive decisions today. 
			The LIVE Competition is like no other – the environment will be comprised of intricate factors whereby the decisions of competitors will influence your own. 
			Uniting the areas of Finance, Marketing, Accounting, Human Resources, and Economics, Delegates will experience the ultimate business challenge.</p>

			<p>The LIVE Competition is set out to emulate a real business environment where in teams of five, Delegates take on the role of managing a company. 
				Each decision made has it's consequences and ultimately these decisions affect the bottom line. Delegates must always think strategically and critically about the multiple effects of their business decisions. 
				They must analyze the market carefully, be confident in communicating their judgments, and always be on their toes for what could be around the corner. 
				In the end, teams will be judged objectively at the hands of industry professionals, based on their performance in a competitive marketplace.</p>

			<p>Most teams will meet expectations and rise up to the challenge. 
				The real leaders are the ones who can adapt to obstacles, perform under stress, and keep their eyes on the prize. 
				Thirty teams will compete but only one will emerge victoriously, and the winning team will take home both prizes and 
				the recognition of having conquered one of the most challenging undergraduate business competitions in the country.</p>


