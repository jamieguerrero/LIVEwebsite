		<h3>Corporate Connections Evening</h3> 
		<p class="lead">The Corporate Connections Evening is a high-profile networking event that provides the opportunity for Delegates to bridge the gap between the academic and professional worlds.</p>
		<p>Delegates interact with top industry professionals, Partner companies, and speakers on areas including company awareness, industries, and recruitment opportunities. 
		This event will provide Delegates with the tools and relationships necessary to facilitate career advancement, while providing Partner companies an opportunity to scout talent.</p>
		<p>Past corporate representatives have included Microsoft, TELUS, EDC, Accenture, BCG, CPPIB, Xerox, Petro-Canada, Toyota, Scotiabank, Deloitte, Ernst & Young, PricewaterhouseCoopers, 
			KPMG, Air Miles, CMA, CGA, ICAO, Procter & Gamble, IBM, TD Securities, Canadian Youth Business Foundation, Blackmont Capital, Claymore Investments, and many more!</p>
