  <h2>
  How to Botch Your Career
  </h2><br><br>

  <img src="http://start-a-business-canada.ca/wp-content/uploads/2012/04/certified-management-accountant.jpg" style="width:300px;height:300px;"/><br><br>

  <p>Josh Linker, author of Disciplined Dreaming, delivered a powerful message at the CMA Ontario Strategic Management Conference earlier this year. Enjoy Josh’s October 2012 Blog Post that will help you Create Possibilities for your career.</p>
<h3>HOW TO BOTCH YOUR CAREER</h3>
<p>Your parents probably gave you loving advice on how to land a job and work your way up the ladder. Unfortunately, that advice is likely dead wrong. While their intentions were probably noble, the world has changed so dramatically in the last few years that the golden nuggets of the past have become the anchors of the present.</p>
<p>In today’s intensely complex, fast, and competitive world, yesterday’s platitudes simply won’t cut it. If you want to reach your full potential, you’ll need to take an entirely different approach. The rate of change today is like none other in history, so it is incumbent upon you to adopt the new rules of engagement. On the other hand, if you are looking for a fast-pass to mediocrity, follow these six time-tested steps to botch your career:</p>
<ol>
<li><strong>Just do what you’re told</strong> – Even though you’ve been taught since grade school to follow instructions, just doing the minimum today will chain your career trajectory to the floor. Instead, you should be delivering each project or assignment with an “and some.” Use each task as an opportunity to contribute above and beyond what was asked. Add a new insight, research finding, or extra goodie, and your work will shine compared to that of your bare-minimum colleagues.</li>
<li><strong>Keep your opinions to yourself</strong> – In the Industrial Age, leaders made all the decisions while workers seldom contributed ideas. In the Brain Age, however, you will advance the fastest if you are a constant source of fresh thinking. Share your perspective and let your imagination shine.</li>
<li><strong>Wait for permission</strong> – Success favors action, so you’re far better off asking for forgiveness than permission. This applies not only to making an impact in your role, but also for your own personal development. Don’t wait for your company or boss to teach you what you need to know. Take full ownership of your own personal growth and development. The library is never closed.</li>
<li><strong>Follow the set path</strong> – Unless you want to slowly climb the soulless ranks, paving your own path is a far better route. Invent a new job, make a business case for radical change, or experiment with a non-traditional approach. The leaders we celebrate – from Edison to Jobs – consistently took the path less travelled.</li>
<li><strong>Embrace loopholes</strong> – You might get off the hook in the near term, but you are far better off taking full responsibility for outcomes instead of covering your ass or pointing fingers. Don’t trade short-term relief for long-term opportunity. Those that reach the highest peaks assume complete accountability for their actions.</li>
<li><strong>Blend in</strong> – Being confused for a colleague or competitor is the highest form of insult. The great ones always stand for something. They cannot be ignored. There is no longer safety in packs; winning today requires standing out as a “category of one.”</li>
</ol>
<p>Old wives tales and traditional thinking have become about as useful as rotary phones. Charles Darwin famously said, “It is not the strongest of the species nor the most intelligent that survives. It is the one that is the most adaptable to change.”</p>
<p>Unless you are content with living small, it’s time to ignore the conventional wisdom of the past and forge ahead to a brighter future. Darwin would be proud!</p>
