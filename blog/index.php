  <h2>
    CMA Ontario – Your Career Partner for Life… Starting Now!
  </h2><br><br>

  <img src="http://start-a-business-canada.ca/wp-content/uploads/2012/04/certified-management-accountant.jpg" style="width:300px;height:300px;"/><br><br>

  <p>As per the fine folks at Wikipedia, &#8220;Recruitment refers to the process of attracting, screening, and selecting a qualified person for a job.&#8221; By very definition, recruitment is a process undertaken by an organization to find the right people &#8211; it is not a process undertaken by people (in your particular case, a UTM student) to find the right organization. In life, however, success in building a great career takes personal effort, persistence and support from valued partners. Relying on recruitment activity from hiring organizations is only part of the process.</p>
<p>As an aspiring business professional, one of the most valuable choices you can make in supporting your career is finding a career partner for life &#8211; a partner that will not only help you develop your skills and competencies to build a strong foundation, but a partner who will support your ongoing professional development so you can evolve and succeed throughout your entire career journey. The Certified Management Accountants of Ontario is one such partner!</p>
<p>Starting today, you can become a CMA Student Associate in order to access great benefits such as the <strong>CMA Student Employment Program</strong>. This program will provide you with weekly updates containing helpful career advice and job postings available to CMA Student Associates and professional program candidates. The table shown here is a small sample of CMA job postings (internship, contract, permanent) from the past three months. As you can see, CMA-bound students &amp; candidates are sought after for a wide variety of roles across many industry segments.</p>
<p>No matter what year you are in, it is valuable to read these job postings to learn more about various positions and discover what jobs interest you. You can see what companies are looking for (e.g. advanced IT / computer skills) and find helpful terms to use on your résumé. You will also discover that many positions combine accounting with other business functions such as marketing, operations, IT, finance and analytics &#8211; a fact that makes CMA a very valuable designation to pursue.</p>
<p>While job postings show you the current hiring needs of an organization, you should also start establishing relationships with employers in order to get to know their corporate culture and better understand if you are a good &#8216;fit&#8217;. Meeting employers at special events and career fairs is a great start. As a CMA Student Associate, you will have special access to &#8220;<strong>Connections 2012</strong>&#8220;, CMA Ontario&#8217;s Fall Career &amp; Networking Fair on Oct. 4 where great employers such as Loblaw, Sun Life, Linamar and Manulife Financial will be present. You can pick-up more career-related info and advice from guest speakers who will be presenting at Connections.</p>
<p>Throughout the year, CMA Student Associates can book 1-on-1 career coaching sessions with the <strong>Career Services Dept. </strong>to improve areas such as your resume, interview skills or LinkedIn profile. Helpful tips will also be sent in the <strong>Weekly Student Job Newsletter</strong>, such as &#8220;What is your greatest weakness?&#8221; &#8211; tips on handling this classic interview question; &#8220;Résumé redundancies&#8221; &#8211; maximize your résumé by avoiding duplication; or &#8220;How To Get Head Hunted Using Your LinkedIn Account&#8221;.</p>
<p>Last, but not least, professional networking can be the most powerful way to connect with new job opportunities. CMA Student Associates can access the <strong>CMA Member Network </strong>both on-line or through CMA district events. Take advantage of this valuable platform to meet CMAs from industries that interest you and start building your professional network. To learn more and sign-up as a CMA Student Associate, please visit www.cmaontario.org and click on &#8220;Become a CMA Student Associate&#8221; under the heading: &#8220;Become A CMA&#8221;. This small investment in your career could provide a big return! Questions? Please contact Mira Sirotic, CMA, msirotic@cmaontario.org, 416-482-5556 or visit www.becomeacma.com.</p>

