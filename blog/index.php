  <h2>
    Career Flexibility
  </h2>

<p><img src="../images/partners/cga.jpg" alt="" title="we_see_more" style="width:600px;height:300px;" class="alignleft size-full wp-image-2785"><br>
Flexibility.</p>
<p>It’s something that can benefit you in both your career and your future studies.</p>
<p>With recruitment season continuing, graduate school fairs and applications around the corner it can be a stressful time for decision making. </p>
<p>Keeping your career options open can pay big dividends in your professional development.</p>
<p>Make sure you participate in as many employer visits, networking sessions and recruitment events as you can on and off campus. You never know who you’ll meet or how it could influence your career path.</p>
<p>Today, accountants can be far from what you would stereotypically expect. Like LIVE Conference, our CGAs span across business in marketing, human resources, tech industry and start-ups. </p>
<p>The need for employees with strong accounting skills that can see more than numbers within the business are vital. </p>
<p>Getting your CGA designation can help you do more with your career and gives you flexibility to pursue your career passions. </p>
<p>Having the ability to complete the CGA program part-time through our Online Learning Environment (OLE) gives you the added flexibility to start your career right from university while gaining industry focused learning.</p>
<p>We’ve got an event coming up with our keynote speaker Greg Twinney, CGA, CFO/COO at Kobo Inc. Before he helped build the Canadian e-reader giant (Kobo) he completed the CGA program while raising capital for several large start-up companies. As a lifelong entrepreneur he’s not exactly your typical accountant.<br />
He is, however; a typical certified general accountant.</p>
<p>Pursuing his career after graduating from college, he leveraged his analytics skills from the CGA program with his passion – in this case, business operations.<br />
Discovering his career path was a journey designed around hard work and flexibility.<br />
My point is, you never know where your career might take you.</p>
<p>keeping your career options flexible can be a major asset in your success.</p>
<p>Enjoy the process of your career development and gain experience in all you do.</p>
