    <h2>
    Simple Recruitment Season Tips
    </h2>

    <img src="../images/partners/cga.jpg" alt="" title="we_see_more" style="width:600px;height:300px;" class="alignleft size-full wp-image-2785">
<p>Recruitment season is in full swing for you guys in a variety of industries from accounting to marketing.</p>
<p>Career fairs, socials, speed networking… Are you ready?</p>
<p>Events like LIVE Conference 2012 are great places to practice your networking skills before that big interview with the firm or CPG recruiter. I thought it would be a good idea to share a few tips I’ve got from some of our certified general accountants (CGAs) that have been through the pressures of recruitment season before.<br />
Body language can play a big part during interviews.</p>
<p>“In my view it’s paramount to your success both professionally and personally,” Marc Morin, CGA and audit team lead at the Canada Revenue Agency (CRA) explains.</p>
<p>As a Toastmaster Society member, Marc’s gained much experience improving his professional communications. As he explains, keeping good posture, eye contact and eating etiquette are all part of the process. Focus on how you’re listening and engaging at events just as much as your verbal communication.</p>
<p>Recruiters see hundreds of candidates and it’s important to set a strong first impression with these simple gestures.</p>
<p>Sean Anklesaria, CGA, business development manager, CGA Ontario, builds relationships with top employers across the industry and used to work for Deloitte.</p>
<p>“Recognize and take stock of the transferable skills you’ve attained on internships, co-ops and in student associations,” Sean says. “Work on your elevator pitch, ask lots of questions and make a great first impression this fall!”</p>
<p>Practice your 30 second elevator pitch at LIVE and make the most of your connections. Follow us on Twitter <a href="http://twitter.com/CGA_DoMore" onclick="javascript:_gaq.push(['_trackEvent','outbound-article','http://twitter.com/CGA_DoMore']);">@CGA_DoMore</a> for more tips. See you there!<br />
- <strong>Jane Annett</strong>, campus recruitment officer, CGA Ontario.</p>
                   
